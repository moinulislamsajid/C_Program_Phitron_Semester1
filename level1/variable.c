#include<stdio.h>
int main(){

    // for int data type 

    int number = 10; // here initialization

    printf("Number is : %d\n",number);


    // for float print

    float weather = 12.23445;

    printf("Todays Weather is : %.3f\n",weather);

    // for double 

    double largerNumber = 12.4542424;
    printf("Large Number is : %lf\n",largerNumber);

    // for character 

    char ch = 's';

    printf("First character is : %c",ch);





}